"""Qt dialog for creating a custom AI-generated desktop character.

The character can be generated by OpenAI (hosted) or by a self-hosted Stable
Diffusion (AUTOMATIC1111) or ComfyUI server. Each backend offers txt2img (prompt
only) and img2img (uses the reference photos), and the self-hosted ones can list
their checkpoints so you can pick a model. All settings persist in config.ini.
"""

from __future__ import annotations

import logging
import os
from pathlib import Path

from PySide6 import QtCore, QtGui, QtWidgets

from . import ai_backends, ai_char, char_catalog, secret_store
from .ui_theme import LIGHT_QSS

logger = logging.getLogger(__name__)

BACKEND_LABELS = [
    ("OpenAI (hosted, transparent)", "openai"),
    ("Stable Diffusion — self-hosted", "a1111"),
    ("ComfyUI — self-hosted", "comfyui"),
]
MODE_LABELS = [
    ("img2img — turn my photos into a cat", "img2img"),
    ("txt2img — from the prompt only", "txt2img"),
]


class GenerationSignals(QtCore.QObject):
    completed = QtCore.Signal(str, str)
    failed = QtCore.Signal(str)


class GenerationWorker(QtCore.QRunnable):
    def __init__(self, name: str, paths: list[Path], settings: dict, api_key: str) -> None:
        super().__init__()
        self.name = name
        self.paths = paths
        self.settings = settings
        self.api_key = api_key
        self.signals = GenerationSignals()

    def run(self) -> None:
        kind = self.settings.get("backend", "openai")
        mode = self.settings.get("mode", "img2img")
        logger.info("Generating a character (backend=%s, mode=%s)", kind, mode)
        try:
            references = ai_char.prepare_references(self.paths) if self.paths else []
            backend = ai_backends.make_backend(self.settings, self.api_key)
            image = backend.generate(references)
            char_id, path = ai_char.install_character(self.name, image)
        except Exception as exc:  # noqa: BLE001 - error is shown in the dialog
            logger.warning("Character generation failed (backend=%s, mode=%s): %s", kind, mode, exc)
            self.signals.failed.emit(str(exc))
        else:
            logger.info("Character generation succeeded: %s", char_id)
            self.signals.completed.emit(char_id, str(path))


class ModelSignals(QtCore.QObject):
    loaded = QtCore.Signal(list)
    failed = QtCore.Signal(str)


class ModelLoadWorker(QtCore.QRunnable):
    def __init__(self, kind: str, url: str) -> None:
        super().__init__()
        self.kind = kind
        self.url = url
        self.signals = ModelSignals()

    def run(self) -> None:
        try:
            models = ai_backends.list_models(self.kind, self.url)
        except Exception as exc:  # noqa: BLE001 - shown in the dialog
            self.signals.failed.emit(str(exc))
        else:
            self.signals.loaded.emit(models)


