    # --- settings <-> widgets -------------------------------------------------
    def combo_select(self, combo: QtWidgets.QComboBox, data: str) -> None:
        index = combo.findData(data)
        if index >= 0:
            combo.setCurrentIndex(index)

    def apply_settings(self) -> None:
        """Populate the widgets from the loaded settings and toggle groups."""
        self.combo_select(self.backend_combo, self.settings.get("backend", "openai"))
        self.combo_select(self.mode_combo, self.settings.get("mode", "img2img"))
        self.combo_select(self.model_combo, self.settings.get("openai_model", ai_backends.OPENAI_MODELS[0]))
        self.combo_select(self.quality_combo, self.settings.get("quality", "low"))
        self.load_local_fields(self.current_kind)
        self.load_prompt_fields(self.current_kind)
        self.update_visibility()

    def style_of(self, kind: str) -> str:
        """Prompt style key: OpenAI has its own; the self-hosted backends share one."""
        return "openai" if kind == "openai" else "selfhosted"

    def load_prompt_fields(self, kind: str) -> None:
        style = self.style_of(kind)
        self.prompt_edit.setPlainText(self.settings.get(f"{style}_prompt", ""))
        self.negative_edit.setPlainText(self.settings.get(f"{style}_negative", ""))

    def stash_prompt_fields(self, kind: str) -> None:
        style = self.style_of(kind)
        self.settings[f"{style}_prompt"] = self.prompt_edit.toPlainText()
        self.settings[f"{style}_negative"] = self.negative_edit.toPlainText()

    def reset_prompts(self) -> None:
        style = self.style_of(self.backend_combo.currentData())
        self.prompt_edit.setPlainText(ai_backends.GENERATION_DEFAULTS[f"{style}_prompt"])
        self.negative_edit.setPlainText(ai_backends.GENERATION_DEFAULTS[f"{style}_negative"])

    def load_local_fields(self, kind: str) -> None:
        if kind in ("a1111", "comfyui"):
            self.url_edit.setText(self.settings.get(f"{kind}_url", ""))
            checkpoint = self.settings.get(f"{kind}_checkpoint", "")
            self.checkpoint_combo.clear()
            if checkpoint:
                self.checkpoint_combo.addItem(checkpoint)
                self.checkpoint_combo.setCurrentText(checkpoint)

    def stash_local_fields(self, kind: str) -> None:
        if kind in ("a1111", "comfyui"):
            self.settings[f"{kind}_url"] = self.url_edit.text().strip()
            self.settings[f"{kind}_checkpoint"] = self.checkpoint_combo.currentText().strip()

    def on_backend_changed(self) -> None:
        self.stash_local_fields(self.current_kind)
        self.stash_prompt_fields(self.current_kind)
        self.current_kind = self.backend_combo.currentData()
        self.load_local_fields(self.current_kind)
        self.load_prompt_fields(self.current_kind)
        self.update_visibility()

    def update_visibility(self) -> None:
        kind = self.backend_combo.currentData()
        self.openai_group.setVisible(kind == "openai")
        self.local_group.setVisible(kind in ("a1111", "comfyui"))

    def collect_settings(self) -> dict:
        self.stash_local_fields(self.current_kind)
        self.stash_prompt_fields(self.current_kind)
        settings = dict(self.settings)
        settings["backend"] = self.backend_combo.currentData()
        settings["mode"] = self.mode_combo.currentData()
        settings["openai_model"] = self.model_combo.currentData()
        settings["quality"] = self.quality_combo.currentData()
        settings["steps"] = str(self.steps_spin.value())
        return settings

    # --- model list -----------------------------------------------------------
    def load_models(self) -> None:
        kind = self.backend_combo.currentData()
        url = self.url_edit.text().strip()
        if kind not in ("a1111", "comfyui"):
            return
        if not url:
            self.set_status("Enter the server address first.", error=True)
            return
        self.load_btn.setEnabled(False)
        self.set_status("Loading models…")
        worker = ModelLoadWorker(kind, url)
        worker.signals.loaded.connect(self.on_models_loaded)
        worker.signals.failed.connect(self.on_models_failed)
        self.pool.start(worker)

    def on_models_loaded(self, models: list) -> None:
        self.load_btn.setEnabled(True)
        current = self.checkpoint_combo.currentText().strip()
        self.checkpoint_combo.clear()
        self.checkpoint_combo.addItems(models)
        if current and current in models:
            self.checkpoint_combo.setCurrentText(current)
        self.set_status(f"Loaded {len(models)} model(s).")

    def on_models_failed(self, message: str) -> None:
        self.load_btn.setEnabled(True)
        self.set_status(message, error=True)

    # --- references -----------------------------------------------------------
    def set_status(self, text: str, *, error: bool = False) -> None:
        self.status_label.setStyleSheet("color: #c0392b;" if error else "color: #555555;")
        self.status_label.setText(text)

    def add_references(self) -> None:
        remaining = ai_char.MAX_REFERENCES - len(self.reference_paths)
        if remaining <= 0:
            self.set_status("You already selected the maximum of 3 photos.", error=True)
            return
        names, _ = QtWidgets.QFileDialog.getOpenFileNames(
            self, "Choose reference photos", "", "Images (*.png *.jpg *.jpeg *.webp);;All files (*)"
        )
        for name in names[:remaining]:
            path = Path(name)
            if path in self.reference_paths:
                continue
            self.reference_paths.append(path)
            item = QtWidgets.QListWidgetItem(QtGui.QIcon(str(path)), path.name)
            item.setData(QtCore.Qt.ItemDataRole.UserRole, str(path))
            self.reference_list.addItem(item)
        if len(names) > remaining:
            self.set_status("Only the first 3 photos were added.")

    def remove_selected(self) -> None:
        for item in self.reference_list.selectedItems():
            path = Path(item.data(QtCore.Qt.ItemDataRole.UserRole))
            if path in self.reference_paths:
                self.reference_paths.remove(path)
            self.reference_list.takeItem(self.reference_list.row(item))

    # --- generate -------------------------------------------------------------
    def generate(self) -> None:
        name = self.name_edit.text().strip()
        try:
            char_id = ai_char.slugify(name)
        except ai_char.AICharError as exc:
            self.set_status(str(exc), error=True)
            return
        settings = self.collect_settings()
        kind = settings["backend"]
        mode = settings["mode"]
        key = self.key_edit.text().strip() or os.getenv("OPENAI_API_KEY", "")

        if mode == "img2img" and not self.reference_paths:
            self.set_status("img2img needs at least one reference photo (or switch to txt2img).", error=True)
            return
        if kind == "openai" and not key:
            self.set_status("Enter an OpenAI API key.", error=True)
            return
        if kind in ("a1111", "comfyui") and not self.url_edit.text().strip():
            self.set_status("Enter the self-hosted server address.", error=True)
            return
        if not self.prompt_edit.toPlainText().strip():
            self.set_status("Enter a prompt.", error=True)
            return
        if char_catalog.find_char(char_id) is not None:
            answer = QtWidgets.QMessageBox.question(
                self, "Replace character?",
                f'A character named "{char_id}" already exists. Replace it with a new generation?',
            )
            if answer != QtWidgets.QMessageBox.StandardButton.Yes:
                return

        if kind == "openai":
            if self.remember_box.isChecked():
                if not secret_store.set_secret(ai_char.SECRET_NAME, key):
                    self.set_status("The keyring could not save the key. Generation will continue.", error=True)
            elif secret_store.get_secret(ai_char.SECRET_NAME):
                secret_store.delete_secret(ai_char.SECRET_NAME)

        ai_backends.save_generation_settings(settings)
        self.generate_btn.setEnabled(False)
        note = " One API request will be charged." if kind == "openai" else ""
        self.set_status("Generating… this can take a bit." + note)
        worker = GenerationWorker(name, list(self.reference_paths), settings, key)
        worker.signals.completed.connect(self.on_completed)
        worker.signals.failed.connect(self.on_failed)
        self.pool.start(worker)

    def on_completed(self, char_id: str, path: str) -> None:
        self.generate_btn.setEnabled(True)
        self.set_status("Saved. Reusing this character does not generate again.")
        self.character_created.emit(char_id)
        self.accept()

    def on_failed(self, message: str) -> None:
        self.generate_btn.setEnabled(True)
        self.set_status(message, error=True)


